function getURLParameter(url, name) {
	return (RegExp(name + '=' + '(.+?)(&|$)').exec(url)||[,null])[1];
}

function loading(){
	$('#prompt').text("loading").show();
	var i = 0;
	var interval = setInterval(function() {
		if($('#prompt').is(':hidden')) clearInterval(interval);
		if(i === 5){
			$('#prompt').text('loading');
			i = 0;
		}
		else{
			$('#prompt').append('.');
			i++;
		}
	}, 750);
}


