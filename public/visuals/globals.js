function loadYoutube(link) {
	loading();
	var url = "public/" + link + ".mp4";
	console.log(url);
	var request = new XMLHttpRequest();
	request.open("GET", url, true);
	request.responseType = "arraybuffer";
	request.send();
	request.onload = function() {
		audioContext.decodeAudioData(request.response, function(buffer) {
			audioBuffer = buffer;
			startSound();
		}, function(e) {
			console.log(e);
		});
	};
};

function loadSampleAudio() {
	loading();
	var audioURL = "public/cohkka.mp3";

	/* Load asynchronously */
	var request = new XMLHttpRequest();
	request.open("GET", audioURL, true);
	request.responseType = "arraybuffer";

	request.onload = function() {
		audioContext.decodeAudioData(request.response, function(buffer) {
			audioBuffer = buffer;
			startSound();
		}, function(e) {
			$('#prompt').text("error loading mp3").show();
			console.log(e);
		});
	};
	request.send();
}

function onDocumentDragOver(evt) {
	evt.stopPropagation();
	evt.preventDefault();
	return false;
}

function onDroppedMP3Loaded(data) {
	audioContext.decodeAudioData(data, function(buffer) {
		audioBuffer = buffer;
		startSound();
	}, function(e) {
		$('#prompt').text("cannot decode mp3").show();
		console.log(e);
	});
}

function onMP3Drop(evt) {
	evt.stopPropagation();
	evt.preventDefault();

	loading();

	var droppedFiles = evt.dataTransfer.files;
	var reader = new FileReader();
	reader.onload = function(fileEvent) {
		var data = fileEvent.target.result;
		onDroppedMP3Loaded(data);
	};
	reader.readAsArrayBuffer(droppedFiles[0]);
}

function startSound() {

	if (source){
		source.stop(0.0);
		source.disconnect();
	}

	/* Connect audio processing graph */
	source = audioContext.createBufferSource();	
	source.connect(audioContext.destination);
	source.connect(analyser);

	$('#prompt').fadeOut('slow');
	source.buffer = audioBuffer;
	source.loop = false;
	source.start(0.0);
	started = true;
	console.log(source);
	/*startViz();*/
}

